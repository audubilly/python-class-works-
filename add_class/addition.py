def add(num1: float, num2: float) -> float:
    num3 = num1 + num2
    return num3


def multiply(num1: float, num2: float) -> float:
    num3 = num1 * num2
    return num3


def sub(num1: float, num2: float) -> float:
    num3 = num1 - num2
    return num3


def div(num1: float, num2: float) -> float:
    num3 = num1 / num2
    return num3


def sqr(num1: float) -> float:
    num2 = num1 ** 2
    return num2


def sqrt(num1: float) -> float:
    num2 = num1 ** 0.5
    return num2
